function updatePage(j) {
	return function() {
		poem = poems[j];
		$("h1").replaceWith("<h1>" + poem.title + "</h1>");
		$(".photo-container").css("background-image", "url(" + poem.img + ")");
		$(".poem").html(poem.text);
		$('.open').removeClass('open');
		$('.active').removeClass('active');
		$("#topic" + poem.topic).addClass('active')
	}
}

$(document).ready(function(){
	for (var i=0; i<poems.length; i++) {
		buttonname = "#btn" + i;
		$(buttonname).click(updatePage(i));
	} 
});
